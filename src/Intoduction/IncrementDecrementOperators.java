package Intoduction;

public class IncrementDecrementOperators {
    public static void main(String[] args) {
        int i=50;
        System.out.println(i++);
        System.out.println(i);

        i=50;
        System.out.println(++i);
        System.out.println(i);

        char ch='A';
        System.out.println(ch++);
        System.out.println(ch);
        System.out.println(++ch);

    }
}
