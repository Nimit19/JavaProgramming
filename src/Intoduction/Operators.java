package com.company;

public class Operators {
    public static void main(String[] args) {

//      Comparison Operators
        System.out.println(19<7);
        System.out.println(9<=9);
        System.out.println(19==7);

//      logical Operators
        System.out.println(19<7 && 19>7);
        System.out.println(19<7 || 19>7);
    }
}
