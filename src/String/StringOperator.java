package String;

public class StringOperator {
    public static void main(String[] args) {
        System.out.println('a');
        System.out.println('b');
        System.out.println('a' + 'b');
        System.out.println('a' + 1);
        System.out.println("a" + "b");
        System.out.println("a" + 1);
    }
}
